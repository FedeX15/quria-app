package com.fexed.quriacompanion;

public class NPC {
    public String name;
    public String clas;
    public String desc;
    public String age;
    public String race;
    public String img;

    public NPC(String name, String clas, String desc, String age, String race, String img) {
        this.name = name;
        this.clas = clas;
        this.desc = desc;
        this.age = age;
        this.race = race;
        this.img = img;
    }
}
